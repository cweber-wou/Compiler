
public class ArrayCheck extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -168548026848395345L;

	public ArrayCheck() {
		super();
	}

	public ArrayCheck(String message) {
		super(message);
		
	}

	public ArrayCheck(Throwable cause) {
		super(cause);
		
	}

	public ArrayCheck(String message, Throwable cause) {
		super(message, cause);
		
	}

}
